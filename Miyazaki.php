            <div class="paragraphes">
                <p>
                    Hayao Miyazaki (宮崎 駿, Miyazaki Hayao), né le 5 janvier 1941 à Tokyo, est un dessinateur, réalisateur et producteur de films d'animation japonais, cofondateur du Studio Ghibli avec Isao Takahata.
                </p>

                <br>

                <p>
                    Presque inconnu en Occident en dehors des cercles d’amateurs d’anime et de manga jusqu’à la sortie internationale de Princesse Mononoké en 1999, ses films rencontrent ensuite un grand succès partout dans le monde et surtout au Japon où certains ont battu
                    des records d’affluence.
                </p>

                <br>

                <p>
                    Il explore souvent les mêmes thèmes centraux, la relation de l’humanité avec la nature, l’écologie et la technologie, ainsi que la difficulté de rester pacifiste dans un monde en guerre. Les protagonistes de ses films sont le plus souvent de jeunes filles
                    ou femmes fortes et indépendantes, et les « méchants » ont des qualités qui les rendent moralement ambigus, comme les kamis de la religion shintoïste.
                </p>

                <br>

                <p>
                    Ses œuvres sont tout aussi accessibles aux enfants qu’aux adultes. Au Japon, il est considéré comme l’égal d’Osamu Tezuka, et en Occident on le compare souvent à Walt Disney1,2,3,4. Toutefois, Miyazaki reste modeste et explique le succès de son entreprise
                    par la chance qu’il a eue de pouvoir exploiter pleinement sa créativité.
                </p>
                <br/>
            </div>
        </section>

       <br/><br/>
        <h3>Le Studio Ghibli</h3>
        <section id="Ghibli">
            <div class="paragraphes">
                <p>Le Studio Ghibli Inc. (株式会社スタジオジブリ, Kabushiki gaisha sutajio Jiburi) est un studio d'animation japonais fondé par Hayao Miyazaki et Isao Takahata en 1985.
                </p>
                <br/>

                <p>
                    Il produit des longs-métrages et courts-métrages d'animation, ainsi que, dans une moindre mesure, des téléfilms, des anime et des jeux vidéo. Le studio est connu principalement pour ses longs-métrages destinés à un large public et dont plusieurs ont remporté
                    des succès auprès de la critique et du public, ainsi que de nombreuses récompenses.
                </p>
                <br/>

                <p>
                    Le logo du studio est une représentation de Totoro, une créature apparue dans Mon voisin Totoro, l'un des films emblématiques du studio, sorti en 1988.
                </p>

                <br/><br/>

                <h4>Nom et Prononciation</h4>

                <p>
                    Le nom du studio provient du mot que l'Italie utilisait pendant la Seconde Guerre mondiale pour désigner l'un de ses avions de reconnaissance, le Caproni Ca.3093 Ghibli. Hayao Miyazaki, grand amateur d'aviation, décide de choisir ce nom.
                </p>
                <br/>

                <p>
                    Pour lui, le studio Ghibli se doit de jouer un rôle d'éclaireur dans le secteur de l'animation japonaise, et d'y faire souffler un vent de nouveauté.
                </p>

                <br/>

                <p>
                    Le nom italien se prononce avec un « g » dur (/ɡ/), alors que le nom japonais se prononce avec un « g » mou (/d͡ʒ/) donnant la prononciation /ˈdʑibɯɺi/. Les Occidentaux ont tendance à utiliser la prononciation issue de l'italien. La prononciation avec
                    un « g » dur a cependant été utilisée par le studio au Japon pour les titres « anglicisés » des courts-métrages Ghiblies et Ghiblies Episode 2, prononcés « giburīzu » (ギブリーズ).
                </p>
            </div>

